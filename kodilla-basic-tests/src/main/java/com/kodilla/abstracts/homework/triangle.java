package com.kodilla.abstracts.homework;

public class triangle extends Shape {
    public triangle(double a,double b) {
        super.bokA = a;
        super.bokB = b;

    }
    double surfaceArea() {
        return bokA = 1/2 * bokA * bokB;

    }

    double circuit() {
        return bokB = bokB + bokA + bokA;
    }
}
